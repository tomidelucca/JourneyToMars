package ss.grupo3.tp4.models;

public class Color {

    private final Double r;
    private final Double g;
    private final Double b;

    public Color(Double r, Double g, Double b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public Double getR() {
        return r;
    }

    public Double getG() {
        return g;
    }

    public Double getB() {
        return b;
    }
}
